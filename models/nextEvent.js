const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const nextEventSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  description: String,
  image: {
    url: String,
    filename: String,
  },
  link: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
});

const NextEvent = mongoose.model("NextEvent", nextEventSchema);
module.exports = NextEvent;